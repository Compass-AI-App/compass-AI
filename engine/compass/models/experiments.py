"""Experiment design models — structured output from the Experimenter engine."""

from __future__ import annotations

from pydantic import BaseModel, Field


class ExperimentDesign(BaseModel):
    """A structured experiment design for validating a product opportunity."""

    title: str
    hypothesis: str = ""
    experiment_type: str = ""  # A/B test, feature flag, user study, etc.
    primary_metric: str = ""
    guardrail_metrics: list[str] = Field(default_factory=list)
    sample_size: str = ""
    duration_estimate: str = ""
    success_criteria: str = ""
    risks: list[str] = Field(default_factory=list)
    evidence_citations: list[str] = Field(default_factory=list)
    recommended_approach: str = ""

    def to_markdown(self) -> str:
        lines = [
            f"# Experiment Design: {self.title}",
            "",
        ]

        if self.hypothesis:
            lines.extend(["## Hypothesis", "", self.hypothesis, ""])

        if self.experiment_type:
            lines.append(f"**Experiment Type:** {self.experiment_type}")
            lines.append("")

        if self.primary_metric:
            lines.extend(["## Primary Metric", "", self.primary_metric, ""])

        if self.guardrail_metrics:
            lines.extend(["## Guardrail Metrics", ""])
            for m in self.guardrail_metrics:
                lines.append(f"- {m}")
            lines.append("")

        if self.sample_size or self.duration_estimate:
            lines.extend(["## Sizing", ""])
            if self.sample_size:
                lines.append(f"**Sample Size:** {self.sample_size}")
            if self.duration_estimate:
                lines.append(f"**Duration:** {self.duration_estimate}")
            lines.append("")

        if self.success_criteria:
            lines.extend(["## Success Criteria", "", self.success_criteria, ""])

        if self.recommended_approach:
            lines.extend(["## Recommended Approach", "", self.recommended_approach, ""])

        if self.risks:
            lines.extend(["## Risks", ""])
            for r in self.risks:
                lines.append(f"- {r}")
            lines.append("")

        if self.evidence_citations:
            lines.extend(["## Evidence", ""])
            for c in self.evidence_citations:
                lines.append(f"- {c}")
            lines.append("")

        lines.extend(["---", "*Generated by Compass Experimenter*"])

        return "\n".join(lines)
